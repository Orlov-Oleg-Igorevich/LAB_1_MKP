/**
 * Компонент информационной подсказки для раздела
 */
interface SectionHelpProps {
  title: string;
  content: string[];
  formulas?: string[];
}

export function SectionHelp({ title, content, formulas }: SectionHelpProps) {
  return (
    <Card withBorder mt="md">
      <Title order={4} mb="md">
        📖 {title}
      </Title>
      {content.map((text, idx) => (
        <Text key={idx} size="sm" mb="xs">
          {text}
        </Text>
      ))}
      {formulas && formulas.length > 0 && (
        <Box mt="md" p="md" style={{ backgroundColor: '#f5f5f5', borderRadius: '4px' }}>
          {formulas.map((formula, idx) => (
            <Text
              key={idx}
              size="sm"
              fw={500}
              style={{ fontFamily: 'monospace', marginBottom: '4px' }}
            >
              {formula}
            </Text>
          ))}
        </Box>
      )}
    </Card>
  );
}
