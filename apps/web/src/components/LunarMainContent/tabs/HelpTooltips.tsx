import { Tooltip, Box, Card, Title, Text } from '@mantine/core';
import { IconInfoCircle } from '@tabler/icons-react';
import type { ReactNode } from 'react';

interface HelpTooltipProps {
  label: string;
  children?: React.ReactNode;
  position?: 'top' | 'right' | 'bottom' | 'left';
  multiline?: boolean;
  width?: number;
}

/**
 * Универсальный компонент подсказки с иконкой информации
 */
export function HelpTooltip({ 
  label, 
  children, 
  position = 'top', 
  multiline = true,
  width = 300 
}: HelpTooltipProps) {
  return (
    <Tooltip
      label={label}
      position={position}
      withArrow
      multiline={multiline}
      maw={width}
      styles={{
        tooltip: {
          backgroundColor: '#1a1a1a',
          color: '#ffffff',
          fontSize: '12px',
          lineHeight: '1.5',
          padding: '10px 12px',
          boxShadow: '0 4px 8px rgba(0,0,0,0.3)',
        },
        arrow: {
          backgroundColor: '#1a1a1a',
        },
      }}
    >
      <Box component="span" style={{ display: 'inline-flex', alignItems: 'center', marginLeft: '6px' }}>
        <IconInfoCircle size={16} color="#666" />
        {children}
      </Box>
    </Tooltip>
  );
}

