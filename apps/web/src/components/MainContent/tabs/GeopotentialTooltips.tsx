import { Tooltip, Box, Card, Title, Text, Group } from '@mantine/core';
import { IconInfoCircle, IconHelp } from '@tabler/icons-react';
import type { ReactNode } from 'react';

interface HelpTooltipProps {
  label: string;
  children?: React.ReactNode;
  position?: 'top' | 'right' | 'bottom' | 'left';
  multiline?: boolean;
  width?: number;
}

/**
 * Универсальный компонент подсказки с космическим дизайном для геопотенциала
 */
export function HelpTooltip({ 
  label, 
  children, 
  position = 'top', 
  multiline = true,
  width = 350 
}: HelpTooltipProps) {
  return (
    <Tooltip
      label={label}
      position={position}
      withArrow
      multiline={multiline}
      maw={width}
      transitionProps={{ duration: 200 }}
      styles={{
        tooltip: {
          background: 'rgba(10, 14, 23, 0.98)',
          backdropFilter: 'blur(12px)',
          color: '#ffffff',
          fontSize: '13px',
          lineHeight: '1.6',
          padding: '14px 16px',
          borderRadius: '12px',
          border: '1px solid rgba(255, 255, 255, 0.1)',
          boxShadow: '0 8px 32px rgba(0, 0, 0, 0.6), 0 0 20px rgba(102, 126, 234, 0.2)',
          fontWeight: 400,
        },
        arrow: {
          backgroundColor: 'rgba(10, 14, 23, 0.98)',
          border: '1px solid rgba(255, 255, 255, 0.1)',
        },
      }}
    >
      <Box component="span" style={{ display: 'inline-flex', alignItems: 'center', marginLeft: '6px' }}>
        <IconInfoCircle size={18} color="#667eea" strokeWidth={2} />
        {children}
      </Box>
    </Tooltip>
  );
}

