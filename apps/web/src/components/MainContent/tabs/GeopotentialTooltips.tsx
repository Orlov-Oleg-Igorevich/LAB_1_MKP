/**
 * Компонент информационной подсказки для раздела с космическим дизайном
 */
interface SectionHelpProps {
  title: string;
  content: string[];
  formulas?: string[];
  icon?: React.ReactNode;
}

export function SectionHelp({ title, content, formulas, icon }: SectionHelpProps) {
  return (
    <Card
      mt="md"
      style={{
        background: 'rgba(255, 255, 255, 0.05)',
        backdropFilter: 'blur(12px)',
        border: '1px solid rgba(255, 255, 255, 0.1)',
        borderRadius: '16px',
        padding: '24px',
      }}
    >
      <Group gap="sm" mb="lg">
        <div
          style={{
            width: '40px',
            height: '40px',
            borderRadius: '12px',
            background: 'linear-gradient(135deg, #667eea 0%, #764ba2 100%)',
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'center',
            boxShadow: '0 4px 12px rgba(102, 126, 234, 0.3)',
          }}
        >
          {icon || <IconHelp size={24} color="white" />}
        </div>
        <Title order={4} style={{ fontSize: '18px', fontWeight: 700 }}>
          📖 {title}
        </Title>
      </Group>
      
      {content.map((text, idx) => (
        <Text key={idx} size="sm" mb="xs" c="gray.3" lh={1.6}>
          {text}
        </Text>
      ))}
      
      {formulas && formulas.length > 0 && (
        <Box 
          mt="md" 
          p="md" 
          style={{ 
            background: 'rgba(10, 14, 23, 0.6)',
            borderRadius: '12px',
            border: '1px solid rgba(102, 126, 234, 0.3)',
          }}
        >
          {formulas.map((formula, idx) => (
            <Text
              key={idx}
              size="sm"
              fw={600}
              style={{ 
                fontFamily: "'JetBrains Mono', monospace",
                marginBottom: '8px',
                color: '#667eea',
              }}
            >
              {formula}
            </Text>
          ))}
        </Box>
      )}
    </Card>
  );
}
